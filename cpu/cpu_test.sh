#!/bin/bash
###################################################################################
# Default Variables
###################################################################################
arch=`uname -m`
max_core_num=`lscpu | grep "CPU(s)" | awk '{print $2}' | head -1`
###################################################################################
# Usage
###################################################################################
Usage()
{
cat << EOF
Example:
     cpu   462  -r 1
     cpu   fp   -r 1
     cpu   int  -r 1
     cpu   all  -r 1
     cpu   all  -r ${max_core_num}

EOF
}

Usage

